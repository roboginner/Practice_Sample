using System;

class Division01
{
    public static void Main()
    {
        Console.WriteLine("10 / 3 = {0}", 10 / 3);
        Console.WriteLine("(10 / 3)の型は{0}", (10 / 3).GetType());
        Console.WriteLine("10 / 3.0 = {0}", 10 / 3.0);
        Console.WriteLine("(10 / 3.0)の型は{0}", (10 / 3.0).GetType());
    }
}